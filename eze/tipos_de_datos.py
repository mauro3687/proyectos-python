#tipo de datos entero o largo
nro = 15
print (nro,type(nro) )

# tipo de datos frotante
nro = 15.5
print (nro, type (nro))

#tipo de dato complejo
nro = 5 + 6j
print(nro, type(nro))


#tipo de nro string
nombre = "ezequiel"
print (nombre, type(nombre))

